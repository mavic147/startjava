package com.startjava.lesson1.variable;

public class Variable {
    public static void main(String[] args) {
        byte coreAmount = 2;
        short ramCapacity = 4;
        int systemType = 64;
        long releaseYear = 2009;
        float discdFreeMemory = 12.6f; //свободно памяти на диске D (в гб)
        double cpuFrequency = 2.60d;
        char winEdition = '7';
        boolean isThermalControl = true;

        System.out.println(coreAmount);
        System.out.println(ramCapacity);
        System.out.println(systemType);
        System.out.println(releaseYear);
        System.out.println(discdFreeMemory);
        System.out.println(cpuFrequency);
        System.out.println(winEdition);
        System.out.println(isThermalControl);
    }
}
